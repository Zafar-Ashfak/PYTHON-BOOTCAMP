# Program to print numbers from 1 to 10 using for loop
print("Program to print numbers from 1 to 10 using for loop")
for i in range(1, 11):
    print(i, end= " ")

# Program to print numbers from 1 to n using for loop by user's choice
print("\nProgram to print numbers from 1 to n using for loop by user's choice")
n = int(input("Enter a number: "))
for j in range(1, n + 1):
    print(j, end= " ")


# Program to print numbers from 1 to 100 with step size of 4 using for loop
print("Program to print numbers from 1 to 100 with step size of 4 using for loop")
for k in range(4, 41, 4):
    print(k, end= " ")
