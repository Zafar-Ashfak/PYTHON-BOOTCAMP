myList = [-12, -4328165, 21, 94, 42103, 544455232, 3434.4359, 729234.25298, True, False, "Python", "Rust", "Go Lang", None]

# Print list's elements using while loop
i = 0
while i < len(myList):
    print(myList[i], end=" ")
    i += 1

print("\n")

# Print list's elements using for loop
for e in myList:
    print(e, end= " ")